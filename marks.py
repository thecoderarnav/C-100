class Marks (object):
    def __init__(self, name, age, gender,marks):
        self.name = name
        self.age = age
        self.gender = gender
        self.marks = marks

Marks = Marks("Arnav ", 14 , "Male",100 ) 
print(Marks.name),
print(Marks.age)
print(Marks.gender)
print(Marks.marks)

